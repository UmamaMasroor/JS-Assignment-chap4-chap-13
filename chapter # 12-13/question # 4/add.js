// Write a program that takes a character (i.e. string of
// length 1) and returns true if it is a vowel, false otherwise

let character = prompt ("Enter a character");

if (
    character === 'a' ||
    character === 'e' ||
    character === 'i' ||
    character === 'o' ||
    character === 'u'
  ) {
   alert("true");
}
else{
    alert("false");
    }

